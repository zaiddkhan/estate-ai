// Priority groupings
export const PRIORITY_AREAS = {
  high: Object.keys(MUMBAI_AREAS).filter(
    (area) => MUMBAI_AREAS[area].priority === "high",
  ),
  medium: Object.keys(MUMBAI_AREAS).filter(
    (area) => MUMBAI_AREAS[area].priority === "medium",
  ),
  low: Object.keys(MUMBAI_AREAS).filter(
    (area) => MUMBAI_AREAS[area].priority === "low",
  ),
};

// Utility functions
export class MumbaiAreasManager {
  constructor() {
    this.areas = MUMBAI_AREAS;
    this.zones = ZONES;
    this.priorities = PRIORITY_AREAS;
  }

  // Generate URL for an area
  generateURL(
    areaName,
    baseUrl = "https://www.nobroker.in/property/rent/mumbai/",
  ) {
    const area = this.areas[areaName];
    if (!area) {
      throw new Error(`Area "${areaName}" not found`);
    }

    const encodedAreaName = encodeURIComponent(areaName);
    return `${baseUrl}${encodedAreaName}?searchParam=${area.searchParam}&radius=2.0&sharedAccomodation=0&city=mumbai&locality=${encodedAreaName}`;
  }

  // Get all areas by priority
  getAreasByPriority(priority = "high") {
    return this.priorities[priority] || [];
  }

  // Get all areas by zone
  getAreasByZone(zoneName) {
    return this.zones[zoneName] || [];
  }

  // Get area details
  getAreaDetails(areaName) {
    return this.areas[areaName] || null;
  }

  // Get all area names
  getAllAreas() {
    return Object.keys(this.areas);
  }

  // Get areas for extraction based on strategy
  getExtractionAreas(strategy = "high-priority") {
    switch (strategy) {
      case "all":
        return this.getAllAreas();
      case "high-priority":
        return this.getAreasByPriority("high");
      case "medium-priority":
        return [
          ...this.getAreasByPriority("high"),
          ...this.getAreasByPriority("medium"),
        ];
      case "south-mumbai":
        return this.getAreasByZone("South Mumbai");
      case "western-suburbs":
        return this.getAreasByZone("Western Suburbs");
      case "eastern-suburbs":
        return this.getAreasByZone("Eastern Suburbs");
      case "central-mumbai":
        return this.getAreasByZone("Central Mumbai");
      case "navi-mumbai":
        return this.getAreasByZone("Navi Mumbai");
      default:
        return this.getAreasByPriority("high");
    }
  }

  // Generate batch URLs for extraction
  generateBatchUrls(areaNames) {
    return areaNames.map((areaName) => ({
      area: areaName,
      url: this.generateURL(areaName),
      details: this.getAreaDetails(areaName),
    }));
  }

  // Get summary statistics
  getSummary() {
    const allAreas = this.getAllAreas();
    const summary = {
      totalAreas: allAreas.length,
      byZone: {},
      byPriority: {
        high: this.priorities.high.length,
        medium: this.priorities.medium.length,
        low: this.priorities.low.length,
      },
    };

    Object.keys(this.zones).forEach((zone) => {
      summary.byZone[zone] = this.zones[zone].length;
    });

    return summary;
  }
}

export default MumbaiAreasManager;
